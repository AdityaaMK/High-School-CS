public class DeclaringInitializingVariables{
	public static void main(String[] args){
		String myName = "Adityaa Magesh Kumar";
		int test1 = 101;
		int test2 = 102;
		int test3 = 999;
		System.out.print("Name:   \t"+myName+"\n");
		System.out.print("Test:   \t"+test1+"\n");
		System.out.print("Test:   \t"+test2+"\n");
		System.out.print("Test:   \t"+test3+"\n\n");
		System.out.print("Sum:    \t" + (test1+test2+test3) + "\n");
		System.out.print("Average:\t" + (test1+test2+test3)/3 + "\n\n");


	}
}